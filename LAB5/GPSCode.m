function code = GPSCode(PRN)
    m = 10;
    
    GPS_SVs = [1 2 6 ; 2 3 7 ; 3 4 8 ; 4 5 9 ;
               5 1 9 ; 6 2 10 ; 7 1 8 ; 8 2 9 ;
               9 3 10 ; 10 2 3 ; 11 3 4 ; 12 5 6 ;
               13 6 7 ; 14 7 8 ; 15 8 9 ; 16 9 10 ;
               17 1 4 ; 18 2 5 ; 19 3 6 ; 20 4 7 ;
               21 5 8 ; 22 6 9 ; 23 1 3 ; 24 4 6 ;
               25 5 7 ; 26 6 8 ; 27 7 9 ; 28 8 10 ;
               29 1 6 ; 30 2 7 ; 31 3 8 ; 32 4 9];
    
    x0 = (-1)*ones(m,1)';
    
    x = x0;
    XG1 = zeros(2^m-1,m);
    XG2 = zeros(2^m-1,m);
    
    XG1(1,:) = x0;
    XG2(1,:) = x0;
    
    %G1
    for k = 2:2^(m)-1
        x = [x(3)*x(10) x(1:m-1)];
        XG1(k,:) = x;
    end
    
    %G2
    for k = 1:2^(m)-1
        x = [x(2)*x(3)*x(6)*x(8)*x(9)*x(10) x(1:m-1)];
        XG2(k,:) = x;
    end

    s1 = GPS_SVs(PRN,2);
    s2 = GPS_SVs(PRN,3);
    code = XG2(:,s1).*XG2(:,s2).*XG1(:,10);
end